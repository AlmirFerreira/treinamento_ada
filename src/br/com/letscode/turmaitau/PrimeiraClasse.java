package br.com.letscode.turmaitau;

public class PrimeiraClasse {

    public static void main(String[] args){

        byte nomeVarByte = '1';
        char nomeVarChar = 'a';

        byte nomeVarByteInv = 123;
        char nomeVarCharInv = 2;

        System.out.println("byte = " + nomeVarByte);
        System.out.println("char = " + nomeVarChar);

        System.out.println("byteInv = " + nomeVarByteInv);
        System.out.println("charInv = " + nomeVarCharInv);
    }
}
